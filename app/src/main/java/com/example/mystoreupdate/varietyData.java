package com.example.mystoreupdate;

public class varietyData {
    String rate,weight;

    public varietyData(String rate, String weight) {
        this.rate = rate;
        this.weight = weight;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }
}
